/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	uint16_t ADC_BatVoltage = 0;
	uint8_t USB_TxBuffer[] = {
			0,			// adc voltage
			0,0			// temp

	};
	//uint8_t T[2];

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  MX_USB_DEVICE_Init();
  MX_ADC1_Init();
  MX_DAC_Init();
  /* USER CODE BEGIN 2 */

  uint8_t tumbler; // tumbler == 1 - discharge, tumbler == 0 - charge

  HAL_ADCEx_Calibration_Start(&hadc1, ADC_SINGLE_ENDED);

  DS18B20_Init(&temperatureSensor, &huart1);


  uint8_t settings[3];
  settings[0] = temperatureSensor.temperatureLimitHigh;
  settings[1] = temperatureSensor.temperatureLimitLow;
  settings[2] = DS18B20_12_BITS_CONFIG;

  DS18B20_InitializationCommand(&temperatureSensor);
  DS18B20_SkipRom(&temperatureSensor);
  DS18B20_WriteScratchpad(&temperatureSensor, settings);



  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, 30);
		ADC_BatVoltage = (uint8_t)HAL_ADC_GetValue(&hadc1);
		HAL_ADC_Stop(&hadc1);

	  	tumbler = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_8);

	  	if(tumbler == 1){
	  	//Discharge
	  		Check_Discharge(&hdac, temperatureSensor.temperature, ADC_BatVoltage, DAC_DISABLE);
	  		Set_Discharge_Current(&hdac, temperatureSensor.temperature, ADC_BatVoltage, DAC_100_MA_Discharge);
	  	}

	  	else{
	  	//Charge
	  		Check_Charge(&hdac, temperatureSensor.temperature, ADC_BatVoltage, DAC_DISABLE);
	  		Set_Charge_Current(&hdac, temperatureSensor.temperature, ADC_BatVoltage, DAC_150_MA_Charge);
	  	}


	  //TEMPERATURE
	    DS18B20_InitializationCommand(&temperatureSensor);
	    DS18B20_SkipRom(&temperatureSensor);
	    DS18B20_ConvertT(&temperatureSensor, DS18B20_DATA);

	    DS18B20_InitializationCommand(&temperatureSensor);
	    DS18B20_SkipRom(&temperatureSensor);
	    //18B20_ReadRom(&temperatureSensor);
	    DS18B20_ReadScratchpad(&temperatureSensor);

	    USB_TxBuffer[0] = (uint8_t)ADC_BatVoltage;				// save voltage to message
	    USB_TxBuffer[1] = (uint8_t)temperatureSensor.MS;
	    USB_TxBuffer[2] = (uint8_t)temperatureSensor.LS;

	    CDC_Transmit_FS(USB_TxBuffer, 3);



		  HAL_Delay(3000);
  }
  /* USER CODE END 3 */
}

